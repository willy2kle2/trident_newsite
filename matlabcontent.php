  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-1 "> <strong> Matlab </strong> is a technical computational software used in 
  Science and Engineering for theoretical analysis , programming and simulation 
  (Simulink). If you are an Engineering student , at some point in your career, 
  maybe your final year project, PGD , Masters or even a PHD research work , 
  you'll be involved in thesis or research work with complex analysis and 
  algorithms where analytical solutions are simply impossible..or extremely
  difficult and pain staking.</p>
  <p class="text-justify pb-1 mb-1 ">
  
Thats where Matlab comes in. Matlab brings a whole lot to the table.
 It would give you very accurate numerical solutions (as well as analytical solutions),
 and you can plot complex graphs in 2D and 3D. You can also write functions and 
 scripts to carry out rigorous analysis , test circuits and run programs for 
 diverse kinds of Engineering applications (Elect , Mech , Aerospace...etc) and designs..etc. 
  </p>
  <p class="text-justify pb-1 mb-1 ">
  Matlab is widely used in research organisations, universities, national laboratories 
  and private companies for various Engineering computations , research and
  development, so your knowledge of Matlab is a plus in your resume anytime.
  </p>
  <p class="text-justify pb-1 mb-0 ">
  Simulink on the other hand is used to model , simulate and analyse dynamic systems 
  (systems whose outputs change over time). Simulink is covered as a full subject in 
  the stage 2 of this Matlab Training course. At the end of this course students would 
  be able to do the following;
  </p>
<ul class="pl-3  pt-0 pb-0">
<li>
Understand the Matlab & Simulink Environment.</li>
<li>Do Math & Computation using Matlab commands</li>
<li>Do Basic and complex plots using Matlab</li>
<li>Apply Matlab to various Numerical Analysis Problems.</li>
<li>Apply Matlab to various Analytical Problems.</li>
<li>Matlab Programming (Scripts & Functions).</li>
<li>Analyse Electrical Circuits using MATLAB.</li>
<li>Apply MATLAB to analysis of Mechanical Sub-Modules.</li>
<li>Use and Apply Simulink System design..</li>
<li>Application Development including GUI.</li>
<li>Algorithm Development.</li>
</ul>

