  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 "> The course is a full fledged practical
  Eelectronics course and has a pre-requisite. You must have done the
  certificate course in electronics ( which gives you good graps of the basics )
  before commencing this course. There are two stages of this training. 
  STAGE 1 delves into advanced topics required for electronics design, 
  including analog & digital circuits, sensors &amp; tranduces, whearas STAGE 2 
  covers micro-controllers and embedded systems. At the end of this course, 
  you be able to ;</p>
<ul class="pl-3  pt-0 pb-0">
<li>
Design basic Analog Circuits using Op-Amps, Transistors etc.</li>
<li>Design Circuits using discrete components.</li>
<li>Write Microcontroller programs in Assembly &amp; C .</li>
<li>Do basic Microcontroller Designs.</li>
<li>Do Electronic circuit simulation using Multisimm.</li>
<li>Do Electronic circuit simulation using Proteus.</li>
<li>Design PCBs and couple projects.</li>
<li>Do Electronics Maintenance and trouble-shooting.</li>

</ul>

