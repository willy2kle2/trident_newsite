  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 "> This is a hands-on practical Fire Alarm
  Systems training. You do not necessarily need to have previous technical
  knowledge or background as you would be taught from the very basics. 
  </p>
  <p>At the end of this course participants would be able to do the following;</p>
<ul class="pl-3  pt-0 pb-0">
<li>
Identify various Fire Alarm Systems' Components, including Control Panels, 
various types of Smoke &amp; Heat Sensors, Notification devices, Power supplies &amp;
 back-up systems. Fire resistant cables.</li>
<li>Understand <strong>Fire Systems topologies </strong>( Conventional, Addressable &amp; 
Stand-Alone modes), concept of Zoning, loops &amp; parallel connections 
for sensors and devices. Concept of Wi-Fyre &amp; Wireless devices. 
Wire to Wireless Gateway. Application of Modems for remote automatic
 (mobile phone notifications).</li>
<li>Understand <strong>Fire Alarm Systems</strong> Design Building safety interfaces; 
Automatica and manually actuated devices. High rise building fire management
 systems.</li>
<li>Alarm Evacuation signals, fire alarm systems code: 
temporal code3, EVAC capabilities pre-alert tones e.t.c.</li>
<li>
<strong> Fire Systems management & control: </strong> Fire warden ststions, 
fire command center,
 pre-Alert tones
</li>


</ul>

