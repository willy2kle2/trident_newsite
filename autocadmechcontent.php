<li>
Start and set-up basic and simple drawings.</li>
<li>Start and set-up basic and simple drawings.</li>
<li>Draw in 2D & 3D.</li>
<li>Do standard Mechanical engineering drawings.</li>
<li>Do standard Assembly drawings</li>
<li>Understand CAD & CAM manufacturing</li>
<li>Do basics of Mechatronics</li>
<li>Communicate and Interpret Engineering Drawings</li>
<li>Produce drawings to a recognised Engineering standards (BS8888).</li>
<li>Do standard electromechanical designs.</li>
<li>Handle mechanical various projects .</li>
<li>Understand how to use Mechanical Engineering Software</li>

</ul>

