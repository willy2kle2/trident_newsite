  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 ">This is a "hands-on" Mechanical Engineering
  course. You have to complete STAGE1 of this course, before doing STAGE2. 
  This course combines the full AutoCAD package with mechanical
  Engineering design and Mechatronics. The problem with learning autoCAD 
  in a computer school is that you only learn how to use the autoCAD 
  software package as a drafting tool, without learning the engineering 
  standards that your drawings have to meet. There is far more to autoCAD
  than that. AutoCAD is not just a drafting software, it is a standard 
  engineering design, production, modelling, programmable and manufacturing 
  (Computer Aided Manufacturing) Software. 
This course is aimed to develop students ability to 
apply autoCAD to mechanical designs and production engineering,
 rather than just see autoCAD as a drafting tool. 
 We are an engineering design company and we use autoCAD extensively
 for most of our applications. Also, besides the fact that we teach autoCAD 
 tailored for Engineering students , we have first hand experience on its
 applications and usage. We have also added aspects of mechatronics to 
 cover areas of electrical knowledge you need for mechanical projects.</p>
 <p>At the end of this course , you'll be able to</p>
