  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-1 "> 
  
  <strong>Mechatronics </strong>is a combination of Mechanical Engineering and Electronics, however as technology advanced,
  Mechatronics has evolved to a multidisciplinary field that encompasses Computer Engineering, Systems Engineering , 
  Telecomms and Control.<br />

Mechatronics brings in Electronics Automation and Electronics control to mechanical designs in a system where the
 multidisciplinary sub fields adapt to each other as they are combined.<br />

Our Mechatronics course is a hands-on practical course that teaches from the very basics of Mechatronics designs'
 to advanced Mechatronics projects.<br />

At the end of this course students would be able to do the following   
  </p>
 
  
 
<ul class="pl-3  pt-0 pb-0">
<li>
Understand the concept of Mechatronics Technology.</li>
<li>Understand , Identify and test basic Electonic components and devices commongly used in Mechtronics.</li>
<li>Do basic AutoCAD drawings in 2D and 3D.</li>
<li>Write Microcontroller Programs in Assembly Language.</li>
<li>Understand and Construct full Electronics and Mechatronics System.</li>
<li>Understand the concept of CNC machine and the mechanical design Automation.</li>


</ul>






