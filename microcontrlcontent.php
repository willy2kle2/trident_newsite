  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-1 "> 
  
  
 	


A <strong> Microcontroller</strong> is a complete computer control in a chip.
 It encompasses a Processor, Memory, and special function registers that that can 
 handle functions like A/D conversion, USART, TIMERS, INTERRUPTS..etc <br />

Our microcontroller training is a hands-on practical training that involves the use of
 PROTEUS & MULTISIMM for simulation as well the use of development board for
 programming and testing , before deployment of the circuit for production.</br>

At the end of this course students would be able to do the following;
  
  </p>
 
  
 
<ul class="pl-3  pt-0 pb-0">
<li>
Understand Microcontroller & Embedded systems basics.</li>
<li>Write Microcontroller programs in Assembly Language.</li>
<li>Write Microcontroller Programs in C Language.</li>
<li>Use of Stand alone programmers and development boards to program different types 
of microcontrollers.</li>
<li>Design microcontroller hardware and interface circuits.</li>
<li>Design Electronics project using Microcontrollers.</li>
<li>Understand the various families of Microcontrollers (PICs , ATMEGA, Arduinos).</li>

</ul>

