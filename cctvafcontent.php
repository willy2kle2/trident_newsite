  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 ">
  This is a hands-on practical (3 in 1) course that teaches you all you need 
  to know about CCTV, Fire Systems & Access Control. You do not necessarily
  need to have previous technical knowledge or background as you would be 
  taught from the very basics. 
  </p>
  <p class="text-justify pb-1 mb-0 ">
  By registering for the (3 in 1) course, you save close to N15K in tuition fees.
  </p>
  <p class="text-justify pb-1 mb-0 ">
  At the end of this course participants would be able to do the following;
  
  </p>
<ul class="pl-3  pt-0 pb-0">
<li>
Identify <strong>CCTV</strong> components and understand their specifications,
 set-up can configure CCTV systems, IP cameras and Remote View
 (Network Applications). Repair, Maintain and Upgrade CCTV, as well as Install 
 and commision CCTV Systems.
</li>
<li>
Identify various <strong>Fire Alarm Systems'</strong> Components, including Control Panels, 
various types of Smoke &amp; Heat Sensors, Notification devices, Power supplies
 &amp;
 back-up systems. Fire resistant cables, Fire Systems topologies 
 ( Conventional, Addressable &amp; Stand-Alone modes), concept of Zoning, 
 loops &amp; parallel connections for sensors and devices. Concept of
 Wi-Fyre &amp;
 Wireless devices. Wire to Wireless Gateway. Application of Modems for remote
 automatic (mobile phone notifications).
</li>
<li>Access Control Components: Control Panels, Power Supplies,Manetic locks, 
Card readers (including Finger Print Time Attendance). 
Access control topologies: Stand alone systems, Networked Systems.</li>
<li>Setting-Up and Installation and configuration of Access Control Systems.</li>

</ul>

