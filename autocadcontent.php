  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 ">The problem with learning autoCAD in a computer school is 
that you only learn how to use the autoCAD software package as a drafting tool 
without understanding the Engineering drawing standards required. There is far
 more to autoCAD than that... Our AutoCAD training is aimed to develop students
 ability to apply autoCAD to their various Engineering field: Achitechtural &
 Building, Civil & Structural, Electrical Engineering, Mechanical & Production
 Engineering, Chemical & Petroleum Engineering. We as an Engineering design company,
 use autoCAD extensively for most of our applications. Also, besides the fact that 
 we teach autoCAD tailored for Engineering students , we have first hand experience 
 on its applications and usage, which means you'll learn more industry based applications.</p>
<ul class="pl-3  pt-0 pb-0">
<li>
Start and set-up basic and simple drawings.</li>
<li>Draw in 2D & 3D</li>
<li>Do standard Engineering drawings.</li>
<li>Do standard Assembly drawings.</li>
<li>Do standard Schematic drawings</li>
<li>Do standard Electrical house wiring drawings</li>
<li>Do Building Plans and Architectural Drawings</li>
<li>Use AutoCAD for Electrical Equipment design</li>
<li>Program Autocad.</li>
<li>Communicate and Interpret Engineering drawings.</li>
<li>Produce drawings to a recognised Engineering standards (BS8888).</li>
</ul>

