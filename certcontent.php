  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 ">The course is a basic introductory course 
  to jump start you on a practical path in electronics. 
  It would not teach you how to build a space craft in 1week!.., 
  but will definitely bring you to the intriguing world of practical 
  electronics and gibe you all the practical basic skills and knowledge that
  you need to hit the ground running in your practical electronics pursuit.
  At the end of this course , students will be able to.</p>
<ul class="pl-3  pt-0 pb-0">
<li>Identify and test basic electronic components.</li>
<li>Use basic test and measuring instruments.</li>
<li>Understand schematic diagrams.</li>
<li>Breadboard simple circuits.</li>
<li>Upgrade existing CCTV installation.</li>
<li>Solder simple circuits.</li>
<li>Build and construct basic electronic circuits.</li>
<li>Embedded systems (Microcontrollers, robotics), electronics design , 
Electronics circuit simulation all done in the Diploma course.</li>

</ul>

