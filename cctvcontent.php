  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 ">This course is a hands-on practical course
  that teaches you all you need to know about CCTV technology.
  You do not necessarily need to have previous technical knowledge or
  background as you would be taught from the very basics. 
At the end of this course participants would be able to do the following;.</p>
<ul class="pl-3  pt-0 pb-0">
<li>Identify CCTV components (cameras, dvrs &amp; NVRs, Cables &amp; Accessories e.t.c)
 and understand their specifications..</li>
<li>Set-up &amp; Configure CCTV System.</li>
<li>Set-up, Configure, and Install IP Cameras.</li>
<li>Troubleshoot , Repair &amp; Maintain CCTV installation.</li>
<li>Upgrade existing CCTV installation.</li>
<li>Set-up &amp; Network CCTV for remote view</li>
<li>Install and comission a CCTV system.</li>

</ul>

