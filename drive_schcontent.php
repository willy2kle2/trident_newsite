  <h4 id="title2"> Learning Outcomes </h4>
  
  
  <p class="text-justify pb-1 mb-0 "> 
Driving a car is not just about your ability to move and control a car!...
As a driver, you have a responsibility for your safety, safety of your 
passengers, other road users and pedestrians.</p>
<p class="text-justify pb-1 mb-0 ">You are also answerable to Law enforcement on the road 
(Police, Road Safety, VIO, LASTMA etc).  
You need to understand driving rules and regulations and how to defend your 
driving (defensive driving). You also need to know what and what is required 
from you by Law enforcement on the road. As well as basic care and maintenance
 of your vehicle.At the end of this driving school course , you'll be able to
 do the following. </p>
 


  
<ul class="pl-3  pt-0 pb-0">
<li>
Drive a Manual & Automatic car.</li>
<li>Understand your responsibilities as a driver.</li>
<li>Learn High way code & High way driving .</li>
<li>Care and maintenance of your vehicle .</li>
<li>Understand the concept of defensive driving.</li>
<li>Learn how to deal with Law enforcement on the road.</li>
<li>Prepare for Driving test and Drivers licence Processing.</li>

</ul>

